package com.accp.domain;

import java.util.Date;

public class Ruzhi {
    private String yuanno;

    private Date ruzhitime;

    private String banno;

    private String remark;

    public String getYuanno() {
        return yuanno;
    }

    public void setYuanno(String yuanno) {
        this.yuanno = yuanno;
    }

    public Date getRuzhitime() {
        return ruzhitime;
    }

    public void setRuzhitime(Date ruzhitime) {
        this.ruzhitime = ruzhitime;
    }

    public String getBanno() {
        return banno;
    }

    public void setBanno(String banno) {
        this.banno = banno;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}