package com.accp.domain;

public class Zhiwu {
    private Integer zhiwuid;

    private String zhiwuname;

    private String gongzi;

    private Integer bumenid;

    private String remark;

    public Integer getZhiwuid() {
        return zhiwuid;
    }

    public void setZhiwuid(Integer zhiwuid) {
        this.zhiwuid = zhiwuid;
    }

    public String getZhiwuname() {
        return zhiwuname;
    }

    public void setZhiwuname(String zhiwuname) {
        this.zhiwuname = zhiwuname;
    }

    public String getGongzi() {
        return gongzi;
    }

    public void setGongzi(String gongzi) {
        this.gongzi = gongzi;
    }

    public Integer getBumenid() {
        return bumenid;
    }

    public void setBumenid(Integer bumenid) {
        this.bumenid = bumenid;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}