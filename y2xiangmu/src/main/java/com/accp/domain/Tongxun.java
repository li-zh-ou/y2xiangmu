package com.accp.domain;

public class Tongxun {
    private String yuangongno;

    private String phone;

    private String zhibo;

    private String shouji;

    private Integer bumenid;

    private String email;

    public String getYuangongno() {
        return yuangongno;
    }

    public void setYuangongno(String yuangongno) {
        this.yuangongno = yuangongno;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getZhibo() {
        return zhibo;
    }

    public void setZhibo(String zhibo) {
        this.zhibo = zhibo;
    }

    public String getShouji() {
        return shouji;
    }

    public void setShouji(String shouji) {
        this.shouji = shouji;
    }

    public Integer getBumenid() {
        return bumenid;
    }

    public void setBumenid(Integer bumenid) {
        this.bumenid = bumenid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}